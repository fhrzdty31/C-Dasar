#include <stdio.h>

int main(void) {
  int x = 5;
  int y = 8;

  printf("Hasil x < y  : %d \n", x < y);
  printf("Hasil x > y  : %d \n", x > y);
  printf("Hasil x <= y : %d \n", x <= y);
  printf("Hasil x >= y : %d \n", x >= y);
  printf("Hasil x == y : %d \n", x == y);
  printf("Hasil x != y : %d \n", x != y);

  return 0;
}